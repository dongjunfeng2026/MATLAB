%% init_WPT_robot.m  ——  值守型光伏清洁机器人无线充电系统参数初始化
%   配套 Simulink 模型：WPT_robot.slx（拓扑：原边 LCC + 副边 S）
%   作者：本科毕业设计仿真平台
%   说明：每次打开 Simulink 模型前先 Run 本脚本，工作区变量会被
%        Series RLC Branch / Mutual Inductance / Pulse Generator 直接引用

clear; clc;

%% 1. 系统额定工况
Vin   = 48;          % 直流母线电压 (V)
Pout  = 200;         % 额定输出功率 (W)
RL    = 10;          % 等效负载电阻 (Ω)
f0    = 85e3;        % 工作频率 (Hz)
w0    = 2*pi*f0;     % 角频率 (rad/s)
Ts    = 1e-7;        % 仿真离散步长 (s) -> ~8.5 点/开关周期

%% 2. 线圈参数
L1    = 100e-6;      % 发射线圈自感 (H)
L2    = 100e-6;      % 接收线圈自感 (H)
R1    = 0.65;        % 发射线圈寄生电阻 (Ω)
R2    = 0.65;        % 接收线圈寄生电阻 (Ω)

%% 3. 原边 LCC 补偿
Lf    = 23e-6;       % 原边谐振电感 (H)
Cf    = 150e-9;      % 原边并联补偿电容 (F)
C1    = 47e-9;       % 原边串联补偿电容 (F)

%% 4. 副边 S 补偿
C2    = 35e-9;       % 副边串联补偿电容 (F)

%% 5. 整流后滤波
Co    = 470e-6;      % 输出滤波电容 (F)

%% 6. 耦合
k     = 0.25;        % 典型耦合系数（机器人对准）
M     = k*sqrt(L1*L2);

%% 7. 全桥逆变 PWM
duty   = 0.5;
Tpwm   = 1/f0;
phase1 = 0;          % Q1+Q4 驱动相位
phase2 = Tpwm/2;     % Q2+Q3 驱动相位（互补）
deadT  = 200e-9;     % 死区（如需）

%% 8. 谐振条件自检
fprintf('---------- 谐振点自检 ----------\n');
fprintf('w0*Lf       = %.3f Ω\n',       w0*Lf);
fprintf('1/(w0*Cf)   = %.3f Ω\n',       1/(w0*Cf));
fprintf('1/(w0^2*C1) = %.3f μH (应≈L1-Lf=%.1fμH)\n', 1/(w0^2*C1)*1e6, (L1-Lf)*1e6);
fprintf('1/(w0^2*C2) = %.3f μH (应≈L2=%.1f μH)\n',   1/(w0^2*C2)*1e6,  L2*1e6);
fprintf('M           = %.3f μH（k=%.2f）\n', M*1e6, k);
fprintf('---------------------------------\n');
